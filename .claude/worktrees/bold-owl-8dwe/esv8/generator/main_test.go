func TestEndpointEntriesFromSchema(t *testing.T) {
	t.Parallel()

	schema := Schema{
		Endpoints: []Endpoint{
			{Name: "search"},
			{Name: "indices.create"},
			{Name: "_internal.get_desired_nodes"},
		},
	}

	entries := make([]endpointEntry, 0, len(schema.Endpoints))
	for _, ep := range schema.Endpoints {
		entries = append(entries, endpointEntry{
			SpecName:   ep.Name,
			MethodName: toMethodName(ep.Name),
		})
	}

	assert.Equal(t, len(entries), 3)
	assert.Equal(t, entries[0].SpecName, "search")
	assert.Equal(t, entries[0].MethodName, "Search")
	assert.Equal(t, entries[1].SpecName, "indices.create")
	assert.Equal(t, entries[1].MethodName, "IndicesCreate")
	assert.Equal(t, entries[2].SpecName, "_internal.get_desired_nodes")
	assert.Equal(t, entries[2].MethodName, "InternalGetDesiredNodes")
}

func TestSchemaJSONUnmarshal(t *testing.T) {
	t.Parallel()

	var schema Schema
	err := json.Unmarshal([]byte(`{"endpoints":[{"name":"search"},{"name":"indices.put_alias"}]}`), &schema)
	assert.NilError(t, err)

	assert.Equal(t, len(schema.Endpoints), 2)
	assert.Equal(t, schema.Endpoints[0].Name, "search")
	assert.Equal(t, schema.Endpoints[1].Name, "indices.put_alias")
}

func TestMainWithLocalSchemaWritesOutput(t *testing.T) {
	t.Parallel()

	tmpDir := t.TempDir()
	schemaPath := filepath.Join(tmpDir, "schema.json")

	err := os.WriteFile(schemaPath, []byte(`{"endpoints":[{"name":"search"},{"name":"indices.put_alias"},{"name":"_internal.get_desired_nodes"}]}`), 0o644)
	assert.NilError(t, err)

	cmd := exec.Command("go", "run", ".", "-schema", schemaPath)
	cmd.Dir = "."
	var stdout bytes.Buffer
	var stderr bytes.Buffer
	cmd.Stdout = &stdout
	cmd.Stderr = &stderr

	err = cmd.Run()
	assert.NilError(t, err)

	outPath := filepath.Join(cmd.Dir, outputFile)
	t.Cleanup(func() {
		_ = os.Remove(outPath)
	})

	generated, err := os.ReadFile(outPath)
	assert.NilError(t, err)

	src := string(generated)
	assert.Assert(t, strings.Contains(stdout.String(), "wrote "+outputFile+" (3 endpoints)"))
	assert.Assert(t, strings.Contains(src, "package esv8_test"))
	assert.Assert(t, strings.Contains(src, `{"search", "Search"}`))
	assert.Assert(t, strings.Contains(src, `{"indices.put_alias", "IndicesPutAlias"}`))
	assert.Assert(t, strings.Contains(src, `{"_internal.get_desired_nodes", "InternalGetDesiredNodes"}`))
	assert.Assert(t, strings.Contains(src, "func TestAPICoverage(t *testing.T)"))
}

