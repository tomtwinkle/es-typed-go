func TestToMethodName(t *testing.T) {
	t.Parallel()

	tests := map[string]struct {
		input string
		want  string
	}{
		"simple": {
			input: "search",
			want:  "Search",
		},
		"namespace": {
			input: "indices.create",
			want:  "IndicesCreate",
		},
		"snake_case": {
			input: "indices.put_alias",
			want:  "IndicesPutAlias",
		},
		"leading_underscore_namespace": {
			input: "_internal.get_desired_nodes",
			want:  "InternalGetDesiredNodes",
		},
		"multiple_empty_segments": {
			input: "._internal..get__desired__nodes.",
			want:  "InternalGetDesiredNodes",
		},
		"only_underscores": {
			input: "___",
			want:  "",
		},
		"empty": {
			input: "",
			want:  "",
		},
	}

	for name, tt := range tests {
		t.Run(name, func(t *testing.T) {
			t.Parallel()
			assert.Equal(t, toMethodName(tt.input), tt.want)
		})
	}
}

func TestTemplateExecuteAndFormat(t *testing.T) {
	t.Parallel()

	data := templateData{
		Endpoints: []endpointEntry{
			{SpecName: "search", MethodName: "Search"},
			{SpecName: "indices.put_alias", MethodName: "IndicesPutAlias"},
			{SpecName: "_internal.get_desired_nodes", MethodName: "InternalGetDesiredNodes"},
		},
	}

	var buf bytes.Buffer
	err := testTemplate.Execute(&buf, data)
	assert.NilError(t, err)

	formatted, err := format.Source(buf.Bytes())
	assert.NilError(t, err)

	got := string(formatted)

	assert.Assert(t, strings.Contains(got, `// Code generated by "go generate"; DO NOT EDIT.`))
	assert.Assert(t, strings.Contains(got, "package esv8_test"))
	assert.Assert(t, strings.Contains(got, `"github.com/tomtwinkle/es-typed-go/esv8"`))
	assert.Assert(t, strings.Contains(got, `{"search", "Search"}`))
	assert.Assert(t, strings.Contains(got, `{"indices.put_alias", "IndicesPutAlias"}`))
	assert.Assert(t, strings.Contains(got, `{"_internal.get_desired_nodes", "InternalGetDesiredNodes"}`))
	assert.Assert(t, strings.Contains(got, "func TestAPICoverage(t *testing.T)"))
	assert.Assert(t, strings.Contains(got, "t.Parallel()"))
}

