// estyped generates type-safe Go field definitions from Elasticsearch index mappings.
//
// Given an Elasticsearch mapping JSON file, it produces a Go source file containing
// typed [estype.Field] definitions for every field found in the mapping.
// This is similar to how sqlc generates Go code from SQL schemas — typos in field
// names become compile errors instead of silent runtime bugs.
//
// There are two input modes:
//
// Mapping mode (default): reads an Elasticsearch mapping JSON file.
//
//	estyped -mapping mapping.json -out model.go -package model
//
// Struct mode: reads JSON struct tags from a Go type definition.
//
//	estyped -struct Product -out model.go
//
// In struct mode the tool parses all .go files in the current directory (or the
// directory of -file), finds the named type, and recursively extracts field paths
// from json struct tags.  This is the recommended usage with go:generate:
//
//	//go:generate go tool estyped -struct Product -out product_fields.go
//
// When the struct (or a pointer to it) implements [estype.ESMappingProvider] by
// defining an ESMapping() method, the generator reads the returned [estype.Mapping]
// to determine the Elasticsearch type of each field.  The method body must contain
// a single return statement with a composite literal; more complex expressions are
// silently ignored and the field type falls back to "unknown".
//
// The Property field of each [estype.MappingField] entry accepts a typed
// property value from a constructor call such as [estype.NewTextProperty] or
// [estype.NewKeywordProperty], or [estype.FieldType] for a plain type name:
//
//	func (Product) ESMapping() estype.Mapping {
//		return estype.Mapping{
//			Fields: []estype.MappingField{
//				{Path: "status", Property: estype.NewKeywordProperty()},
//				{Path: "title",  Property: estype.NewTextProperty()},
//				{Path: "price",  Property: estype.FieldType("integer")},
//			},
//		}
//	}
//
// There are two output modes:
//
// Constant mode (default): generates individual constants.
//
//	estyped -mapping mapping.json -out model.go -package model
//
// Struct mode: generates a struct variable so fields are accessed as model.Sample.FieldName.
//
//	estyped -mapping mapping.json -out model.go -package model -name Sample
//
// The mapping JSON can be in either the full Get Mapping API format:
//
//	{"mappings": {"properties": { ... }}}
//
// or the abbreviated properties-only format:
//
//	{"properties": { ... }}
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"log"
	"os"
	"path/filepath"
	"sort"
	"strings"
	"text/template"

	"github.com/tomtwinkle/es-typed-go/estype"
)

type fieldEntry struct {
	ConstName string
	FieldName string
	Path      string
	Type      string
}

type templateData struct {
	Package string
	Name    string
	Fields  []fieldEntry
}

// constTemplate generates individual constants (default mode).
var constTemplate = template.Must(template.New("const").Parse(`// Code generated by estyped; DO NOT EDIT.

package {{ .Package }}

import "github.com/tomtwinkle/es-typed-go/estype"

{{ range .Fields -}}
// Field{{ .ConstName }} is the "{{ .Path }}" field (type: {{ .Type }}).
const Field{{ .ConstName }} estype.Field = "{{ .Path }}"
{{ end -}}
`))

// structTemplate generates a struct variable for grouped field access (struct mode).
var structTemplate = template.Must(template.New("struct").Parse(`// Code generated by estyped; DO NOT EDIT.

package {{ .Package }}

import "github.com/tomtwinkle/es-typed-go/estype"

// {{ .Name }} provides typed field names for the Elasticsearch index mapping.
var {{ .Name }} = struct {
{{- range .Fields }}
	{{ .FieldName }} estype.Field
{{- end }}
}{
{{- range .Fields }}
	{{ .FieldName }}: "{{ .Path }}",
{{- end }}
}
`))

func main() {
	mappingPath := flag.String("mapping", "", "path to Elasticsearch mapping JSON file")
	structType := flag.String("struct", "", "Go type name to read JSON struct tags from (alternative to -mapping)")
	filePath := flag.String("file", "", "Go source file for struct lookup (used with -struct; defaults to current directory)")
	outPath := flag.String("out", "", "output Go file path (required)")
	pkgName := flag.String("package", "", "Go package name for the generated file (defaults to $GOPACKAGE)")
	structName := flag.String("name", "", "struct variable name for grouped field access (optional; omit for constant mode)")
	flag.Parse()

	// When invoked via go:generate, $GOPACKAGE is set automatically.
	if *pkgName == "" {
		*pkgName = os.Getenv("GOPACKAGE")
	}

	if *outPath == "" || *pkgName == "" {
		flag.Usage()
		os.Exit(1)
	}
	if *mappingPath == "" && *structType == "" {
		fmt.Fprintln(os.Stderr, "error: either -mapping or -struct is required")
		flag.Usage()
		os.Exit(1)
	}
	if *mappingPath != "" && *structType != "" {
		fmt.Fprintln(os.Stderr, "error: -mapping and -struct are mutually exclusive")
		flag.Usage()
		os.Exit(1)
	}

	var entries []fieldEntry

	if *structType != "" {
		// Struct-based parsing: extract field paths from JSON struct tags.
		srcDir := "."
		if *filePath != "" {
			srcDir = filepath.Dir(*filePath)
		}
		var err error
		entries, err = parseGoStruct(srcDir, *structType)
		if err != nil {
			log.Fatalf("failed to parse struct %s: %v", *structType, err)
		}
	} else {
		// Mapping-based parsing: existing behaviour.
		data, err := os.ReadFile(*mappingPath)
		if err != nil {
			log.Fatalf("failed to read mapping file %s: %v", *mappingPath, err)
		}

		mapping, err := estype.ParseMapping(data)
		if err != nil {
			log.Fatalf("failed to parse mapping: %v", err)
		}

		entries = make([]fieldEntry, 0, len(mapping.Fields))
		for _, f := range mapping.Fields {
			entries = append(entries, fieldEntry{
				ConstName: toPascalCase(f.Path),
				FieldName: toStructFieldName(f.Path),
				Path:      f.Path,
				Type:      fieldType(f.TypeName()),
			})
		}
	}

	var buf bytes.Buffer
	td := templateData{
		Package: *pkgName,
		Name:    *structName,
		Fields:  entries,
	}

	if *structName != "" {
		if err := structTemplate.Execute(&buf, td); err != nil {
			log.Fatalf("template execution failed: %v", err)
		}
	} else {
		if err := constTemplate.Execute(&buf, td); err != nil {
			log.Fatalf("template execution failed: %v", err)
		}
	}

	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		log.Fatalf("go/format failed: %v\n\nGenerated source:\n%s", err, buf.String())
	}

	if err := os.WriteFile(*outPath, formatted, 0o644); err != nil {
		log.Fatalf("failed to write %s: %v", *outPath, err)
	}

	fmt.Printf("wrote %s (%d fields)\n", *outPath, len(entries))
}

// toPascalCase converts a dot-separated ES field path to PascalCase.
// Used for constant names where all segments are concatenated.
// Examples:
//
//	"status"        → "Status"
//	"items.color"   → "ItemsColor"
//	"title.keyword" → "TitleKeyword"
func toPascalCase(path string) string {
	parts := strings.Split(path, ".")
	var result []string
	for _, part := range parts {
		subParts := strings.Split(part, "_")
		for _, sp := range subParts {
			if sp == "" {
				continue
			}
			result = append(result, strings.ToUpper(sp[:1])+sp[1:])
		}
	}
	return strings.Join(result, "")
}

// toStructFieldName converts a dot-separated ES field path to a struct field name.
// Each path segment is PascalCased and segments are joined with "_" to preserve
// the nesting structure.
// Examples:
//
//	"status"        → "Status"
//	"items.color"   → "Items_Color"
//	"field_name"    → "FieldName"
func toStructFieldName(path string) string {
	parts := strings.Split(path, ".")
	var result []string
	for _, part := range parts {
		subParts := strings.Split(part, "_")
		var segment []string
		for _, sp := range subParts {
			if sp == "" {
				continue
			}
			segment = append(segment, strings.ToUpper(sp[:1])+sp[1:])
		}
		result = append(result, strings.Join(segment, ""))
	}
	return strings.Join(result, "_")
}

// fieldType returns the type string for display, defaulting to "object" when empty.
func fieldType(t string) string {
	if t == "" {
		return "object"
	}
	return t
}

// parseGoStruct extracts fieldEntry values from a named Go struct type by reading
// JSON struct tags from all .go files found in srcDir.  If the struct (or a
// pointer to it) has an ESMapping() method, its return value is parsed
// statically to determine the Elasticsearch type of each field.
func parseGoStruct(srcDir, typeName string) ([]fieldEntry, error) {
	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(fset, srcDir, nil, 0)
	if err != nil {
		return nil, fmt.Errorf("failed to parse Go source in %s: %w", srcDir, err)
	}

	// Build a map from type name to struct AST node across all packages/files.
	typeMap := make(map[string]*ast.StructType)
	for _, pkg := range pkgs {
		for _, file := range pkg.Files {
			collectStructTypes(file, typeMap)
		}
	}

	if _, ok := typeMap[typeName]; !ok {
		return nil, fmt.Errorf("type %q not found in %s", typeName, srcDir)
	}

	// Extract ES field types from the optional ESMapping() method.
	mappingTypes := extractESMappingMethod(pkgs, typeName)

	var entries []fieldEntry
	extractGoStructEntries(typeName, "", typeMap, mappingTypes, &entries)
	sort.Slice(entries, func(i, j int) bool {
		return entries[i].Path < entries[j].Path
	})
	return entries, nil
}

// collectStructTypes adds every struct type declaration found in f to typeMap.
func collectStructTypes(f *ast.File, typeMap map[string]*ast.StructType) {
	for _, decl := range f.Decls {
		gd, ok := decl.(*ast.GenDecl)
		if !ok || gd.Tok != token.TYPE {
			continue
		}
		for _, spec := range gd.Specs {
			ts, ok := spec.(*ast.TypeSpec)
			if !ok {
				continue
			}
			st, ok := ts.Type.(*ast.StructType)
			if !ok {
				continue
			}
			typeMap[ts.Name.Name] = st
		}
	}
}

// extractGoStructEntries recursively walks the named struct and appends fieldEntry
// values for every JSON-visible field.  mappingTypes maps field paths to their
// Elasticsearch type string; entries from ESMapping() override defaults derived
// from the Go type.
func extractGoStructEntries(typeName, prefix string, typeMap map[string]*ast.StructType, mappingTypes map[string]string, entries *[]fieldEntry) {
	st, ok := typeMap[typeName]
	if !ok {
		return
	}
	for _, field := range st.Fields.List {
		// Anonymous embedded field: inline its fields at the same nesting level.
		if len(field.Names) == 0 {
			if name := derefTypeName(field.Type); name != "" {
				extractGoStructEntries(name, prefix, typeMap, mappingTypes, entries)
			}
			continue
		}

		jsonName := goFieldJSONName(field)
		if jsonName == "-" {
			continue
		}

		path := jsonName
		if prefix != "" {
			path = prefix + "." + jsonName
		}

		// When the element type is a known struct, recurse into it.
		elemName := derefTypeName(field.Type)
		if _, isStruct := typeMap[elemName]; isStruct {
			esType := "object"
			if isSliceExpr(field.Type) {
				esType = "nested"
			}
			// Allow ESMapping() to override the derived type.
			if t, ok := mappingTypes[path]; ok {
				esType = t
			}
			*entries = append(*entries, fieldEntry{
				ConstName: toPascalCase(path),
				FieldName: toStructFieldName(path),
				Path:      path,
				Type:      esType,
			})
			extractGoStructEntries(elemName, path, typeMap, mappingTypes, entries)
			continue
		}

		// Leaf field — use type from ESMapping() if available, otherwise "unknown".
		esType := "unknown"
		if t, ok := mappingTypes[path]; ok {
			esType = t
		}
		*entries = append(*entries, fieldEntry{
			ConstName: toPascalCase(path),
			FieldName: toStructFieldName(path),
			Path:      path,
			Type:      esType,
		})
	}
}

// goFieldJSONName returns the JSON key name for a struct field.
// It reads the json struct tag when present; otherwise it falls back to the Go
// field name.  It returns "-" when the field is explicitly excluded with json:"-".
func goFieldJSONName(field *ast.Field) string {
	if field.Tag != nil {
		name := jsonTagKey(strings.Trim(field.Tag.Value, "`"))
		if name != "" {
			return name
		}
	}
	if len(field.Names) > 0 {
		return field.Names[0].Name
	}
	return ""
}

// jsonTagKey parses a raw struct tag string (with backticks already stripped) and
// returns the JSON key name.  It returns "-" to signal the field must be skipped,
// and "" when no json tag is present or when the tag name is empty (e.g. ",omitempty").
func jsonTagKey(raw string) string {
	const prefix = `json:"`
	idx := strings.Index(raw, prefix)
	if idx < 0 {
		return ""
	}
	rest := raw[idx+len(prefix):]
	end := strings.IndexByte(rest, '"')
	if end < 0 {
		return ""
	}
	name, _, _ := strings.Cut(rest[:end], ",")
	return name
}

// derefTypeName unwraps pointer, slice, and array wrappers and returns the base
// type identifier name.  It returns "" for qualified (selector) expressions such
// as time.Time.
func derefTypeName(expr ast.Expr) string {
	switch t := expr.(type) {
	case *ast.Ident:
		return t.Name
	case *ast.StarExpr:
		return derefTypeName(t.X)
	case *ast.ArrayType:
		return derefTypeName(t.Elt)
	}
	return ""
}

// isSliceExpr returns true when the outermost non-pointer type is a slice
// (variable-length, i.e. ArrayType with Len == nil).  Fixed-length arrays
// and non-array types return false.
func isSliceExpr(expr ast.Expr) bool {
	switch t := expr.(type) {
	case *ast.StarExpr:
		return isSliceExpr(t.X)
	case *ast.ArrayType:
		return t.Len == nil
	}
	return false
}

// extractESMappingMethod searches the parsed packages for a method named
// "ESMapping" with a value or pointer receiver of the given type name.  It
// parses the method body with [parseESMappingBody] and returns a map from
// field path to Elasticsearch type string.  An empty map is returned when
// the method is absent or its body cannot be statically analysed.
func extractESMappingMethod(pkgs map[string]*ast.Package, typeName string) map[string]string {
	fieldTypes := make(map[string]string)
	for _, pkg := range pkgs {
		for _, file := range pkg.Files {
			for _, decl := range file.Decls {
				fd, ok := decl.(*ast.FuncDecl)
				if !ok || fd.Recv == nil || fd.Name.Name != "ESMapping" || fd.Body == nil {
					continue
				}
				for _, recv := range fd.Recv.List {
					if derefTypeName(recv.Type) != typeName {
						continue
					}
					parseESMappingBody(fd.Body, fieldTypes)
					return fieldTypes
				}
			}
		}
	}
	return fieldTypes
}

// parseESMappingBody extracts field path→type pairs from the body of an
// ESMapping() method.  It expects a single return statement whose result is a
// composite literal that constructs an estype.Mapping value.
//
// The Property field of each MappingField entry is resolved to an ES type name
// in two ways:
//
//   - A plain string literal (e.g. Property: "integer") is used directly.
//   - A call to a NewXxxProperty constructor (e.g. Property: estype.NewTextProperty(...))
//     is resolved by extracting "Xxx" from the function name and converting it
//     to snake_case (e.g. "Text" → "text", "DenseVector" → "dense_vector").
//
// Any element that cannot be statically resolved is silently skipped.
func parseESMappingBody(body *ast.BlockStmt, out map[string]string) {
	for _, stmt := range body.List {
		ret, ok := stmt.(*ast.ReturnStmt)
		if !ok || len(ret.Results) != 1 {
			continue
		}
		mappingLit, ok := ret.Results[0].(*ast.CompositeLit)
		if !ok {
			continue
		}
		// Look for the "Fields" key in the outer composite literal.
		for _, elt := range mappingLit.Elts {
			kv, ok := elt.(*ast.KeyValueExpr)
			if !ok {
				continue
			}
			key, ok := kv.Key.(*ast.Ident)
			if !ok || key.Name != "Fields" {
				continue
			}
			// The value should be a slice composite literal.
			fieldSlice, ok := kv.Value.(*ast.CompositeLit)
			if !ok {
				continue
			}
			for _, fieldElt := range fieldSlice.Elts {
				fieldLit, ok := fieldElt.(*ast.CompositeLit)
				if !ok {
					continue
				}
				var path, esType string
				for _, fElt := range fieldLit.Elts {
					fkv, ok := fElt.(*ast.KeyValueExpr)
					if !ok {
						continue
					}
					fKey, ok := fkv.Key.(*ast.Ident)
					if !ok {
						continue
					}
					switch fKey.Name {
					case "Path":
						if bl, ok := fkv.Value.(*ast.BasicLit); ok && bl.Kind == token.STRING {
							path = strings.Trim(bl.Value, `"`)
						}
					case "Property":
						esType = propertyValueTypeName(fkv.Value)
					}
				}
				if path != "" && esType != "" {
					out[path] = esType
				}
			}
		}
	}
}

// propertyValueTypeName extracts the Elasticsearch type name from an AST
// expression used as the Property field of an estype.MappingField literal.
// It handles three forms:
//   - A [estype.FieldType] conversion: FieldType("integer") or estype.FieldType("integer")
//   - A constructor call: NewTextProperty(...) or estype.NewTextProperty(...);
//     the ES type name is derived from the constructor function name.
//   - A plain string literal: "integer" (accepted for resilience when parsing
//     code that predates the [estype.FieldType] introduction).
func propertyValueTypeName(expr ast.Expr) string {
	call, ok := expr.(*ast.CallExpr)
	if !ok {
		// Plain string literal fallback.
		if bl, ok := expr.(*ast.BasicLit); ok && bl.Kind == token.STRING {
			return strings.Trim(bl.Value, `"`)
		}
		return ""
	}
	// Detect FieldType("...") or estype.FieldType("...") conversion calls.
	if name := fieldTypeFuncName(call.Fun); name == "FieldType" {
		if len(call.Args) == 1 {
			if bl, ok := call.Args[0].(*ast.BasicLit); ok && bl.Kind == token.STRING {
				return strings.Trim(bl.Value, `"`)
			}
		}
		return ""
	}
	// Fall through to constructor call detection.
	return propertyCallTypeName(call.Fun)
}

// fieldTypeFuncName returns the simple function name from an expression if it
// is an identifier or selector (e.g. both "FieldType" and "estype.FieldType"
// return "FieldType").  Returns "" for any other expression shape.
func fieldTypeFuncName(expr ast.Expr) string {
	switch e := expr.(type) {
	case *ast.Ident:
		return e.Name
	case *ast.SelectorExpr:
		return e.Sel.Name
	}
	return ""
}

// propertyCallTypeName derives the Elasticsearch type name from the function
// expression of a NewXxxProperty constructor call.
// The function may be an unqualified identifier (NewTextProperty) or a selector
// expression (estype.NewTextProperty, esv8.NewTextProperty, etc.).
// Returns "" when the expression does not match the New…Property pattern.
func propertyCallTypeName(expr ast.Expr) string {
	var name string
	switch e := expr.(type) {
	case *ast.Ident:
		name = e.Name
	case *ast.SelectorExpr:
		name = e.Sel.Name
	}
	if !strings.HasPrefix(name, "New") || !strings.HasSuffix(name, "Property") {
		return ""
	}
	// Extract the PascalCase type fragment between "New" and "Property".
	middle := name[3 : len(name)-8]
	if middle == "" {
		return ""
	}
	return pascalToSnake(middle)
}

// pascalToSnake converts a PascalCase string to snake_case.
// Used to derive an Elasticsearch type name from a Go constructor name fragment.
// Examples:
//
//	"Text"        → "text"
//	"Keyword"     → "keyword"
//	"DenseVector" → "dense_vector"
func pascalToSnake(s string) string {
	result := make([]byte, 0, len(s)+4)
	for i := 0; i < len(s); i++ {
		c := s[i]
		if i > 0 && c >= 'A' && c <= 'Z' {
			result = append(result, '_')
		}
		if c >= 'A' && c <= 'Z' {
			result = append(result, c + 32) // to lower
		} else {
			result = append(result, c)
		}
	}
	return string(result)
}
